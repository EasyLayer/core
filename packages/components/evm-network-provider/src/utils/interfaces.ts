/**
 * Интерфейс для логов событий, полученных от ноды.
 */
export interface Log {
  /** Адрес контракта, который эмитировал событие */
  address: string;
  /** Массив topics; topic[0] — сигнатура события, последующие — индексированные параметры */
  topics: string[];
  /** Закодированные неиндексированные данные события */
  data: string;
}
